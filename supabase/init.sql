-- ════════════════════════════════════════════════════════════════════════════
--  FLC Check-In — full database initialisation
--
--  This is the single source of truth. Run it once on a fresh Supabase
--  project (paste into the SQL editor, or `supabase db push` if you have
--  the CLI wired up). It supersedes every prior schema/migration file.
--
--  Idempotent: every CREATE uses IF NOT EXISTS or CREATE OR REPLACE, so it's
--  safe to re-run on an already-initialised database without losing data.
--
--  What this sets up:
--    • Tables: member_profiles, checkin_events, checkin_records,
--              event_scope_members, checkin_attempts, checkin_devices,
--              face_match_claims, absence_notes, audit_log
--    • Helpers: point_in_polygon, haversine_meters, point_in_event_geofence
--    • RPCs:    create_checkin_event, reset_event_pin, record_pin_attempt,
--              claim_device_for_event, claim_face_match,
--              report_member_location, auto_checkout_expired_events,
--              submit_checkin
--    • Grants for the anon role used by the @supabase/supabase-js client
--
--  Design notes:
--    • RLS is OFF. All access goes through security-definer RPCs that
--      enforce auth themselves.
--    • Member IDs are TEXT (FLC graph uses MongoDB ObjectIds, not UUIDs).
--      Event IDs are UUID (generated by Postgres).
--    • PINs are rotating 6-digit OTPs derived from the event's qr_secret —
--      not stored hashes. The pin_hash/pin_set_at columns are kept for
--      back-compat but the rotating-PIN path doesn't use them.
-- ════════════════════════════════════════════════════════════════════════════


