-- ─── record_pin_attempt: bcrypt PIN with rate limiting ──────────────────────
-- Kept for back-compat / direct callers; the rotating-PIN path in
-- submit_checkin does not use this.
create or replace function public.record_pin_attempt(
  p_event_id   uuid,
  p_member_id  text,
  p_pin_plain  text
) returns jsonb
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  v_event           public.checkin_events%rowtype;
  v_lockout_until   timestamptz;
  v_attempts_in_win int;
  v_attempts_left   int;
  v_match           boolean;
begin
  select * into v_event from public.checkin_events where id = p_event_id;
  if not found then
    return jsonb_build_object('ok', false, 'reason', 'event_not_found');
  end if;
  if v_event.status <> 'ACTIVE' then
    return jsonb_build_object('ok', false, 'reason', 'event_not_active', 'status', v_event.status);
  end if;
  if v_event.pin_hash is null then
    return jsonb_build_object('ok', false, 'reason', 'pin_not_set');
  end if;

  select max(lockout_until) into v_lockout_until
    from public.checkin_attempts
   where event_id = p_event_id
     and member_id = p_member_id
     and lockout_until is not null
     and lockout_until > now();
  if v_lockout_until is not null then
    return jsonb_build_object('ok', false, 'reason', 'locked_out', 'lockout_until', v_lockout_until);
  end if;

  v_match := (extensions.crypt(p_pin_plain, v_event.pin_hash) = v_event.pin_hash);

  if v_match then
    insert into public.checkin_attempts (event_id, member_id, success)
      values (p_event_id, p_member_id, true);
    return jsonb_build_object('ok', true);
  end if;

  select count(*) into v_attempts_in_win
    from public.checkin_attempts
   where event_id = p_event_id
     and member_id = p_member_id
     and success = false
     and attempted_at > now() - interval '10 minutes';

  if v_attempts_in_win + 1 >= 5 then
    insert into public.checkin_attempts (event_id, member_id, success, lockout_until)
      values (p_event_id, p_member_id, false, now() + interval '15 minutes');
    return jsonb_build_object('ok', false, 'reason', 'locked_out',
                              'lockout_until', now() + interval '15 minutes');
  end if;

  insert into public.checkin_attempts (event_id, member_id, success)
    values (p_event_id, p_member_id, false);
  v_attempts_left := 5 - (v_attempts_in_win + 1);
  return jsonb_build_object('ok', false, 'reason', 'wrong_pin', 'attempts_left', v_attempts_left);
end;
$$;


-- ─── claim_device_for_event: one device per member per event ────────────────
create or replace function public.claim_device_for_event(
  p_event_id     uuid,
  p_fingerprint  text,
  p_member_id    text
) returns boolean
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  v_existing text;
begin
  insert into public.checkin_devices (event_id, device_fingerprint, member_id)
    values (p_event_id, p_fingerprint, p_member_id)
    on conflict (event_id, device_fingerprint) do nothing;

  select member_id into v_existing
    from public.checkin_devices
   where event_id = p_event_id and device_fingerprint = p_fingerprint;

  return v_existing = p_member_id;
end;
$$;


-- ─── claim_face_match: short-lived FACE_ID gate ─────────────────────────────
create or replace function public.claim_face_match(
  p_event_id  uuid,
  p_member_id text
) returns jsonb
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.face_match_claims (event_id, member_id, claimed_at)
  values (p_event_id, p_member_id, now())
  on conflict (event_id, member_id)
    do update set claimed_at = now();
  return jsonb_build_object('ok', true);
exception
  when others then
    return jsonb_build_object('ok', false, 'reason', 'server_error', 'detail', sqlerrm);
end;
$$;


-- ─── report_member_location: location heartbeat + auto-checkout ─────────────
create or replace function public.report_member_location(
  p_event_id  uuid,
  p_member_id text,
  p_lat       double precision,
  p_lng       double precision
) returns jsonb
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  v_inside boolean;
  v_was_checked_out boolean := false;
begin
  v_inside := public.point_in_event_geofence(p_event_id, p_lat, p_lng);
  if not v_inside then
    update public.checkin_records
       set checked_out_at  = now(),
           auto_checked_out = true
     where event_id = p_event_id
       and member_id = p_member_id
       and checked_out_at is null;
    if found then v_was_checked_out := true; end if;
  end if;
  return jsonb_build_object('inside_fence', v_inside, 'checked_out', v_was_checked_out);
end;
$$;


-- ─── auto_checkout_expired_events: cron job for ended events ────────────────
create or replace function public.auto_checkout_expired_events()
returns int
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  v_count int := 0;
begin
  with closed as (
    update public.checkin_records cr
       set checked_out_at  = now(),
           auto_checked_out = true
      from public.checkin_events ce
     where cr.event_id = ce.id
       and ce.status = 'ACTIVE'
       and ce.ends_at <= now()
       and cr.checked_out_at is null
    returning cr.id
  )
  select count(*) into v_count from closed;

  update public.checkin_events
     set status = 'ENDED'
   where status = 'ACTIVE'
     and ends_at <= now();

  return v_count;
end;
$$;


-- ─── create_checkin_event: hashes the optional PIN server-side ──────────────
create or replace function public.create_checkin_event(
  p_name                     text,
  p_event_type               text,
  p_scope_level              text,
  p_scope_church_id          text,
  p_scope_church_name        text,
  p_venue_name               text,
  p_starts_at                timestamptz,
  p_ends_at                  timestamptz,
  p_grace_period_min         int,
  p_auto_checkout_min        int,
  p_allowed_check_in_methods text[],
  p_allowed_roles            text[],
  p_geofence_type            text,
  p_geofence_center_lat      double precision,
  p_geofence_center_lng      double precision,
  p_geofence_radius_m        int,
  p_geofence_polygon         jsonb,
  p_pin_plain                text,
  p_qr_secret_hex            text,
  p_created_by_id            text,
  p_created_by_name          text
) returns uuid
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  v_id uuid;
  v_pin_hash text := null;
begin
  if p_pin_plain is not null then
    v_pin_hash := extensions.crypt(p_pin_plain, extensions.gen_salt('bf', 10));
  end if;

  insert into public.checkin_events (
    name, event_type, scope_level, scope_church_id, scope_church_name, venue_name,
    starts_at, ends_at, grace_period_min, auto_checkout_min,
    allowed_check_in_methods, allowed_roles,
    geofence_type, geofence_center_lat, geofence_center_lng, geofence_radius_m, geofence_polygon,
    pin_hash, pin_set_at, qr_secret,
    created_by_id, created_by_name
  ) values (
    p_name, p_event_type, p_scope_level, p_scope_church_id, p_scope_church_name, p_venue_name,
    p_starts_at, p_ends_at, p_grace_period_min, p_auto_checkout_min,
    p_allowed_check_in_methods, p_allowed_roles,
    p_geofence_type, p_geofence_center_lat, p_geofence_center_lng, p_geofence_radius_m, p_geofence_polygon,
    v_pin_hash, case when p_pin_plain is not null then now() else null end, decode(p_qr_secret_hex, 'hex'),
    p_created_by_id, p_created_by_name
  ) returning id into v_id;

  return v_id;
end;
$$;


-- ─── reset_event_pin: re-hash a new PIN and clear lockouts ──────────────────
create or replace function public.reset_event_pin(
  p_event_id uuid,
  p_pin_plain text
) returns void
language plpgsql
security definer
set search_path = public, extensions
as $$
begin
  update public.checkin_events
     set pin_hash   = extensions.crypt(p_pin_plain, extensions.gen_salt('bf', 10)),
         pin_set_at = now()
   where id = p_event_id;

  update public.checkin_attempts
     set lockout_until = null
   where event_id = p_event_id
     and lockout_until is not null
     and lockout_until > now();
end;
$$;


-- ─── submit_checkin: single entry point for QR / PIN / FACE_ID / MANUAL ─────
-- All validation is performed atomically inside this function. PIN uses a
-- rotating 6-digit OTP derived from qr_secret (15s window, current or
-- previous bucket accepted). QR uses HMAC over 'eventId:bucket' (60s window).
-- FACE_ID requires a fresh face_match_claim (<60s) and consumes it.
create or replace function public.submit_checkin(
  p_event_id     uuid,
  p_member_id    text,
  p_member_name  text,
  p_member_role  text,
  p_member_unit  text,
  p_method       text,
  p_lat          double precision,
  p_lng          double precision,
  p_fingerprint  text,
  p_qr_token     text default null,
  p_pin_plain    text default null
) returns jsonb
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  v_event          public.checkin_events%rowtype;
  v_now            timestamptz := now();
  v_qr_bucket_now  bigint;
  v_pin_bucket_now bigint;
  v_parts          text[];
  v_token_event_id text;
  v_token_bucket   bigint;
  v_token_sig_hex  text;
  v_expected_sig   bytea;
  v_otp_hmac       bytea;
  v_otp_int        bigint;
  v_otp_cur        text;
  v_otp_prev       text;
  v_in_fence       boolean;
  v_device_ok      boolean;
  v_is_late        boolean;
  v_record_id      uuid;
  v_claim_age      interval;
begin
  -- 1. Fetch event
  select * into v_event from public.checkin_events where id = p_event_id;
  if not found then
    return jsonb_build_object('ok', false, 'reason', 'event_not_found');
  end if;

  -- 2. Status and time window
  --    Check-in opens 1 hour before starts_at and closes at ends_at.
  if v_event.status = 'PAUSED' then
    return jsonb_build_object('ok', false, 'reason', 'event_paused');
  end if;
  if v_event.status = 'ENDED' then
    return jsonb_build_object('ok', false, 'reason', 'event_ended');
  end if;
  if v_now < (v_event.starts_at - interval '1 hour') then
    return jsonb_build_object(
      'ok',         false,
      'reason',     'not_started',
      'opens_at',   (v_event.starts_at - interval '1 hour')
    );
  end if;
  if v_now > v_event.ends_at then
    return jsonb_build_object('ok', false, 'reason', 'event_ended');
  end if;

  -- 3. Method allowed
  if not (p_method = any(v_event.allowed_check_in_methods)) then
    return jsonb_build_object('ok', false, 'reason', 'method_not_allowed');
  end if;

  -- 4. Method-specific verification
  if p_method = 'QR' then
    if p_qr_token is null then
      return jsonb_build_object('ok', false, 'reason', 'missing_qr_token');
    end if;

    v_parts := string_to_array(p_qr_token, ':');
    if array_length(v_parts, 1) <> 3 then
      return jsonb_build_object('ok', false, 'reason', 'invalid_qr_token');
    end if;

    v_token_event_id := v_parts[1];
    v_token_bucket   := v_parts[2]::bigint;
    v_token_sig_hex  := lower(v_parts[3]);

    if v_token_event_id <> p_event_id::text then
      return jsonb_build_object('ok', false, 'reason', 'invalid_qr_token');
    end if;

    v_qr_bucket_now := floor(extract(epoch from v_now) / 60)::bigint;
    if v_token_bucket <> v_qr_bucket_now and v_token_bucket <> (v_qr_bucket_now - 1) then
      return jsonb_build_object('ok', false, 'reason', 'qr_expired');
    end if;

    v_expected_sig := extensions.hmac(
      convert_to(v_token_event_id || ':' || v_token_bucket::text, 'UTF8'),
      v_event.qr_secret,
      'sha256'
    );
    if encode(v_expected_sig, 'hex') <> v_token_sig_hex then
      return jsonb_build_object('ok', false, 'reason', 'invalid_qr_token');
    end if;

  elsif p_method = 'PIN' then
    if p_pin_plain is null then
      return jsonb_build_object('ok', false, 'reason', 'missing_pin');
    end if;

    v_pin_bucket_now := floor(extract(epoch from v_now) / 15)::bigint;

    v_otp_hmac := extensions.hmac(
      convert_to(p_event_id::text || ':' || v_pin_bucket_now::text, 'UTF8'),
      v_event.qr_secret, 'sha256'
    );
    v_otp_int := (('x' || right(encode(v_otp_hmac, 'hex'), 8))::bit(32)::int4::bigint
                  + 4294967296) % 4294967296 % 1000000;
    v_otp_cur := lpad(v_otp_int::text, 6, '0');

    v_otp_hmac := extensions.hmac(
      convert_to(p_event_id::text || ':' || (v_pin_bucket_now - 1)::text, 'UTF8'),
      v_event.qr_secret, 'sha256'
    );
    v_otp_int := (('x' || right(encode(v_otp_hmac, 'hex'), 8))::bit(32)::int4::bigint
                  + 4294967296) % 4294967296 % 1000000;
    v_otp_prev := lpad(v_otp_int::text, 6, '0');

    if p_pin_plain <> v_otp_cur and p_pin_plain <> v_otp_prev then
      return jsonb_build_object('ok', false, 'reason', 'wrong_pin');
    end if;

  elsif p_method = 'FACE_ID' then
    -- Require a fresh claim (<60s). Consume it whether or not the rest of
    -- the check-in succeeds (the client must call claim_face_match again
    -- to retry).
    select v_now - claimed_at into v_claim_age
      from public.face_match_claims
     where event_id = p_event_id and member_id = p_member_id;

    if v_claim_age is null then
      return jsonb_build_object('ok', false, 'reason', 'face_match_required');
    end if;

    if v_claim_age > interval '60 seconds' then
      delete from public.face_match_claims
       where event_id = p_event_id and member_id = p_member_id;
      return jsonb_build_object('ok', false, 'reason', 'face_match_expired');
    end if;

    delete from public.face_match_claims
     where event_id = p_event_id and member_id = p_member_id;

  else
    -- MANUAL goes through submit_manual_check_in (direct insert by admin) — never
    -- through this RPC. Any other method is unknown.
    return jsonb_build_object('ok', false, 'reason', 'unsupported_method');
  end if;

  -- 5. Geofence
  v_in_fence := public.point_in_event_geofence(p_event_id, p_lat, p_lng);
  if not v_in_fence then
    return jsonb_build_object('ok', false, 'reason', 'outside_fence');
  end if;

  -- 6. Device fingerprint claim — skipped for MANUAL (admin shared kiosk).
  if p_method <> 'MANUAL' then
    v_device_ok := public.claim_device_for_event(p_event_id, p_fingerprint, p_member_id);
    if not v_device_ok then
      return jsonb_build_object('ok', false, 'reason', 'device_already_used');
    end if;
  end if;

  -- 7. Late detection — relative to starts_at, not the early window.
  --    Someone checking in before starts_at is never late.
  v_is_late := v_now > (v_event.starts_at + (v_event.grace_period_min * interval '1 minute'));

  -- 8. Insert record
  insert into public.checkin_records (
    event_id, member_id, member_name, member_role, member_unit_name,
    method, geo_verified, check_in_lat, check_in_lng,
    device_fingerprint, is_late
  ) values (
    p_event_id, p_member_id, p_member_name, p_member_role, p_member_unit,
    p_method, true, p_lat, p_lng, p_fingerprint, v_is_late
  )
  returning id into v_record_id;

  return jsonb_build_object(
    'ok', true,
    'record', jsonb_build_object(
      'id',      v_record_id,
      'is_late', v_is_late,
      'method',  p_method
    )
  );

exception
  when unique_violation then
    return jsonb_build_object('ok', false, 'reason', 'already_checked_in');
  when others then
    return jsonb_build_object('ok', false, 'reason', 'server_error', 'detail', sqlerrm);
end;
$$;


-- ─── is_super_admin(p_email) ─────────────────────────────────────────────────
-- Security-definer: runs as postgres so it can read the superadmins table
-- even though anon has no direct SELECT on it. search_path pinned to prevent
-- search-path injection.
create or replace function public.is_super_admin(p_email text)
  returns boolean
  language plpgsql
  security definer
  set search_path = public, extensions
as $$
begin
  return exists (
    select 1 from public.superadmins
    where email = lower(trim(p_email))
  );
end;
$$;


